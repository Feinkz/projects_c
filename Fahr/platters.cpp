#include <iostream>
#include <locale>
using namespace std;
int main()
{
	setlocale(LC_ALL, "RUS");
	int speed;
	cout << "\nВведите скорость вращения пластинки 33,45 или 78: ";
	cin >> speed;
	switch (speed)
	{
	case 33:
		cout << "Долгоиграющий формат\n";
		break;
	case 45:
		cout << "Формат сигнала\n";
		break;
	case 78:
		cout << "Устаревший формат\n";
		break;
	}
	return 0;
}